    public boolean checkMH(String maMH){
        boolean ok=false;
        try {
            NguoiDungManager ndm=new NguoiDungManager();
            if(!ndm.timkiemMH(maMH)){
                ok=true;
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DSMH.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(DSMH.class.getName()).log(Level.SEVERE, null, ex);
        }
        return ok;
    }
