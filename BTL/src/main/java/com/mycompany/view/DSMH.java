    public void initTable(){
 
        model= new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Mã Môn Học","Tên Môn Học","Số Tín Chỉ"});
        try {
            NguoiDungManager ndm=new NguoiDungManager();
             List<Mon> list = new ArrayList<>();
             list=ndm.layDSMH();
             list.forEach(item->{
                 model.addRow(new Object[]{item.getMaMH(),item.getTenMH(),item.getSoTC()});
             });
             JTB.setModel(model);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DSMH.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(DSMH.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

