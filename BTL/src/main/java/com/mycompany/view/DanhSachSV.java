    public boolean checkmsv(String msv){
        boolean ok=false;
        try {
            NguoiDungManager ndm=new NguoiDungManager();
            if(ndm.timKiem(msv)==null){
                ok=true;
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DanhSachSV.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(DanhSachSV.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return ok;
    }
