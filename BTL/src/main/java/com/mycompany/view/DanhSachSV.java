    public boolean check(){
        boolean ok=false;
        String regex="[0-9]{10}";
        if(JT2.getText().equalsIgnoreCase("")){
            ok=true;
            JL2.setText("Mã Sinh Viên Không Được Để Trống!");
        }
        else if(!JT2.getText().matches(regex)){
            ok=true;
            JL2.setText("Mã Sinh Viên Không Hợp Lệ!");
        }
         else{
            JL2.setText("");
        }
        return ok;
    }
