    public void initTable() throws ClassNotFoundException, SQLException{
        NguoiDungManager ng=new NguoiDungManager();
        model=new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Mã Sinh Viên","Họ Tên","Email","Số Điện Thoại","Giới Tính","Địa Chỉ"});
        List<SinhVien> list=ng.layDSSV();
        list.forEach(item->{
        model.addRow(new Object[]{item.getMaSV(),item.getHoTen(),item.getEmail(),item.getSdt(),item.getGioiTinh()==1?"Nam":"Nữ",item.getDiaChi()});
        });
        JTB.setModel(model);
    }
